package JavaBasics;

public class if_else_PositiveNegative {

	public static void main(String[] args) {
		
		double db = 0;
		
		if(db>0.0) {
			System.out.println("Number is positive");
		}else if(db<0.0) {
			System.out.println("Number is negative");
		}else {
			System.out.println("Number is zero");
		}

	}

}
